            // Password validation for signup
            document.getElementById('signupPasswordInput').addEventListener('input', function(e) {
                const password = e.target.value;
                const minChar = document.getElementById('minChar');
                const caseCheck = document.getElementById('caseCheck');
                const numberCheck = document.getElementById('numberCheck');

                // Minimum 8 characters
                if (password.length >= 8) {
                    minChar.parentElement.classList.add('valid');
                } else {
                    minChar.parentElement.classList.remove('valid');
                }

                // Upper and lower case
                if (/[a-z]/.test(password) && /[A-Z]/.test(password)) {
                    caseCheck.parentElement.classList.add('valid');
                } else {
                    caseCheck.parentElement.classList.remove('valid');
                }

                // At least 1 number
                if (/\d/.test(password)) {
                    numberCheck.parentElement.classList.add('valid');
                } else {
                    numberCheck.parentElement.classList.remove('valid');
                }
            });

